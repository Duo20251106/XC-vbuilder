import { ref } from 'vue'

export const useDisclosure = (opened?: Boolean) => {
    const isOpen = ref(opened ?? false)

    const open = () => {
        isOpen.value = true
    }
    const close = () => {
        isOpen.value = false
    }
    const toggle = () => {
        isOpen.value = !isOpen.value
    }

    return { isOpen, open, close, toggle }
}
